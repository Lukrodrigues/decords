			<p>
				O Projeto dos Grupos de Violão da Escola Ministro Rubem Carlos Ludwig é conduzido pelo Professor de Música José A. Homrich – Formado em Licenciatura em Música pela UFRGS
				e Pós-Graduando do Curso de Educação Inclusiva.
				Surgiu após o primeiro chamamento de professores de Música para o município de Canoas. Conforme a Lei Nº 11.769 de Agosto de 2008 a inserção da música nas escolas é obrigatório,
				tendo como último prazo o início do Ano de 2012. Dessa forma o Professor José A. Homrich (Licenciado em Música pela UFRGS) iniciou seus trabalhos com música na Escola Ministro Ludwig a partir do mês de Julho de 2012.</p>
			<p>
				Sendo uma disciplina nova no currículo das escolas e tendo obrigatoriedade nas escolas, logo percebeu-se diversas possibilidades para que essa inserção fosse com boa qualidade,
				mesmo tendo pouco material didático especializado produzido. Sendo Licenciado em Música e tendo habilitação e larga experiência em instrumentos de corda (Violão, Guitarra e Canto) e canto, logo ficou evidenciado que em turmas com grande número de alunos e ainda não tendo qualquer instrumento musical a disposição seria inviável atender ao apelo dos alunos que gostariam, segundo os diagnósticos aplicados pelo professor com as turmas no começo de cada ano de trabalho, a aprendizagem mais focada a um instrumento musical específico seria menos inviável. Tendo em vista as habilidades musicais com instrumento de corda do professor começou a surgir a idéia da implantação de grupos de violão.
			</p>

			<p>
				O projeto foi idealizado no ano de 2012 quando o professor José Homrich – primeiro professor de Música do município de Canoas,
				juntamente com o professor Alexandre Rodrigues - iniciou seus trabalhos musicais na disciplina de Artes na escola Ministro Rubem Carlos Ludwig.
				Até aquele momento a base dos planos de estudos da escola em questão e do município era as Artes Visuais.
			</p>

			<p>
				Após o início das aulas de Artes voltadas para a música percebeu-se uma grande dificuldade: por conta das turmas serem numerosas e as salas pequenas
				e de não haverem instrumentos musicais disponíveis, percebeu-se que, mesmo se houvesse a possibilidade de adquirir tais instrumentos não haveria espaço físico para utilizá-los.
				Inicialmente o professor José, em combinação com a Diretora Letícia analisou a possibilidade de formar alguns grupos de instrumentistas num turno inverso, porém,
				em virtude da requisição do professor para trabalhar juntamente com Secretaria de Educação do município (SME) não houve a possibilidade naquele momento da conclusão do projeto original.
			</p>

			<p>
				No início do ano seguinte a diretora da escola manteve contato com a Secretaria de Educação e conseguiu a liberação do professor José para uma manhã de trabalho
				na escola com o Projeto de Grupo de Violões. Após a confirmação de que seria viável a construção do Projeto, o professor fez orçamentos, pesquisou preços de violões acústicos e elétricos,
				além de acessórios para aula de música e, através de verbas da própria escola foram adquiridos e o projeto tomou forma, iniciando em Março de 2013.
			</p>
			<p>
				Inicialmente foram feitos convites aos alunos para a aula de instrumento na própria sala de aula e dessa forma pensou-se em dois grupos de violão: um para trabalhar conceitos e
				iniciação musical através do violão com um repertório mais simplificado e outro grupo que já praticava violão a um determinado tempo e que teria um repertório violonístico mais elaborado.
				O segundo grupo que era menor e composto por alunos que já tinham contato com o instrumento em poucas semanas já estava apresentando-se na escola, durante a celebração da Páscoa (música composta pelo Professor José).
				Um dos alunos que apresentou-se, visivelmente emocionado agradeceu pela oportunidade de estar ali, fazendo música e tocando para um grande público (formado por alunos da escola e seus familiares).
				Após menos de dois meses de aprendizagem musical ao violão o Grupo de Violões contando com 16 instrumentos (seis deles pertencentes à escola e três pertencentes ao professor)
				apresentou-se nas comemorações do Dia das Mães da Escola Ministro Rubem Carlos Ludwig (novamente com música composta pelo Professor José).
			</p>
			<p>
				Atualmente o Grupo tem aproximadamente 50 integrantes dentre violonistas e alguns cantores de um grupo vocal formado em 2015.
				São atendidos alunos da escola e nos shows que o grupo atua como convidado, recebe apoio instrumental de alguns alunos que já se formaram na escola,
				mas que mantém o vínculo com a escola por residirem na comunidade escolar e terem laços de amizade com alunos e professores. O grupo tem se dedicado ao estudo
				da parte técnica do instrumento e montado repertório a partir de composições próprias e de músicas Nacionais e Internacionais de diversos estilos e gêneros para apresentações musicais.
			</p>
